<h2>¿Cómo  hacer la credencial?</h2>

<div class="informacion">
	<p>
		Se le pide al lector dos fotografías tamaño infantil y se llena una tarjeta de registro donde se le anota su nombre completo, domicilio, teléfono, ocupación, escuela o trabajo dirección y teléfono de esta última. Se anota la fecha de expedición la firma del lector y la firma del encargado de la biblioteca. Así como los mismos datos de un fiador de preferencia mayor de 18 años quien será el responsable de cumplir con el cuidado y entrega del material en caso de que el lector no lo haga. 
		Una vez con todos los datos del lector se le realiza una credencial donde va incluida su fotografía, nombre completo, domicilio,  fecha de vencimiento y las firmas del lector y encargado de la biblioteca.
	</p>

	<h4>Al contar el lector con su credencial se acredita para:</h4>
	<ul>
		<li>Obtener en préstamo a domicilio hasta tres libros simultáneamente durante una semana.</li>
		<li>Renovar el préstamo siempre que otra persona no lo haya solicitado.</li>
		<li>Apartar libros que se encuentren prestados.</li>
	</ul>

	<h4>El lector se obliga a:</h4>
	<ul>
		<li>Conservar en buen estado los materiales de la biblioteca.</li>
		<li>Devolver los libros prestados a domicilio en la fecha indicada.</li>
	</ul>
</div>

