<h2>Préstamos</h2>
<h3>Sala</h3>
<div class="informacion">
<p>Brinda al público en general la oportunidad de consultar los materiales dispuestos en estantería abierta, 
además de aquellos que se encuentran en el Área de Consulta.</p>

<h4>Los recursos que pueden ser consultados son:</h4>
<ul>
	<li>Libros</li>
	<li>CD, DVD, Blue Ray</li>
	<li>Materiales de consulta (diccionarios, atlas, enciclopedias, biografías, etc.)</li>
	<li>Mapas</li>
	<li>Publicaciones periódicas: revistas, entre otros.</li>
</ul>

<h3>Domicilio</h3>
<p>
Consiste en la autorización que se otorga a los usuarios para llevar fuera de la 
Biblioteca Lic. Pedro Vélez y Zúñiga los materiales de la colección general, 
colección infántil, Braille, con excepción de las publicaciones periódicas y las obras 
de consulta.
</p>

<h4>Para este servicio se requiere:</h4>
<ul>
	<li>Tramitar la credencial de la biblioteca.</li>
	<li>El usuario puede obtener en préstamo a domicilio hasta tres recursos de información. </li>
	<li>El periodo de préstamo a domicilio de los libros es de siete días con opción a dos renovaciones por el mismo periodo de tiempo.</li>
	<li>El usuario debe verificar las condiciones físicas de los materiales que ha obtenido en préstamo a domicilio, puesto que al recibirlos se hace responsable de cualquier daño que pudieran sufrir.</li>
	<li>El usuario está obligado a devolver en la fecha señalada los recursos de información obtenidos en préstamo a domicilio.</li>
	<li>En caso de que el libro que el usuario requiera se encuentre prestado, puede solicitar la reservación del mismo a fin de que, en cuanto sea devuelto, se ponga a su disposición.</li>
	<li>El usuario debe recoger el libro en la fecha que se le indique, de lo contrario se cancelará la reservación.</li>
</ul>

<h2>Préstamos interbibliotecarios</h2>
<h4>Consiste en obtener recursos de información que pertenecen a otra Biblioteca con la que la Biblioteca Lic. Pedro Vélez y Zúñiga ténga convenio. Es necesario 
considerar lo siguiente:</h4>
<ul>
	<li>Contar con la credencial de la Biblioteca Lic. Pedro Vélez y Zúñiga vigente y sin sanciones por atraso en la entrega del material. </li>
	<li>Revisar que el libro requerido, no se encuentre en las colecciones de la Biblioteca.</li>
	<li>Tener los datos completos del libro solicitado (autor, título, clasificación e Institución donde se localiza).</li>
	<li>El tipo y la cantidad de los recursos de información disponibles para préstamo lo establecerá la Biblioteca prestataria.</li>
	<li>El tiempo máximo del préstamo lo determina la Biblioteca titular y responsable del material. </li>
	<li>La Biblioteca Lic. Pedro Vélez y Zúñiga es el área encargada del trámite y el usuario es responsable de la integridad física de los materiales. </li>
	<li>En caso de pérdida o daño al material que se obtiene en préstamo, el usuario será quien cubra los gastos derivados de esto. </li>
	<li>Entregar en tiempo y forma el material en la Institución que otorgó el préstamo; así mismo, se deberá traer el comprobante de devolución. </li>
</ul>
